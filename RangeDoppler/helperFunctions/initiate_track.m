function track=initiate_track(meas,N,lambda,CPI)

P = diag([5000.0, 22.5, 0.04, 0.1]);
F1 = [1,0,-lambda*CPI, 0;...
    0, 0,-lambda,-lambda*CPI;...
    0,0,1,CPI;...
    0,0,0,1];
F2 = [1,CPI,0,0; 0,1,0,0; 0,0,1,CPI; 0,0,0,1];
Q = diag([3000.0, 0.02, 0.2, 0.05]);
H = [1,0,0,0; 0,0,1,0];
R = diag([2500, 10]);
S = diag([1, 1]);
kalman_dtype=struct('x',[meas(1); -lambda*meas(2); meas(2); -1],'P',P,'F1',F1,'F2',F2,'H',H,'Q',Q,'R',R,'S',S,...
                    'hist_meas',[meas(1);  meas(2); ],'hist_kf_out',[meas(1); -lambda*meas(2); meas(2); -1],...
                    'hist_kf_est',[])
track=struct('lifespan',0,'range',meas(1),'doppler',meas(2),'estimate',meas(1:2).','state',-1,'assoc_cand',[],'init_vec',[1 ;zeros(N-1,1)],...
                     'kalman_state',kalman_dtype); 
end